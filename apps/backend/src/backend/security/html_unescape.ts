// Byte-exact port of CPython's `html.unescape` (Lib/html/__init__.py, frozen interpreter 3.14.x).
//
// SECURITY-CRITICAL: this feeds `stripPrivilegedTags` in the trust-tier input-wrapping subsystem.
// Any divergence from CPython is a parity finding. The algorithm is reproduced step-for-step:
//
//   def unescape(s):
//       if '&' not in s: return s
//       return _charref.sub(_replace_charref, s)
//
//   _charref = re.compile(r'&(#[0-9]+;?|#[xX][0-9a-fA-F]+;?|[^\t\n\f <&#;]{1,32};?)')
//
//   def _replace_charref(s):
//       s = s.group(1)
//       if s[0] == '#':
//           if s[1] in 'xX': num = int(s[2:].rstrip(';'), 16)
//           else:            num = int(s[1:].rstrip(';'))
//           if num in _invalid_charrefs:                       return _invalid_charrefs[num]
//           if 0xD800 <= num <= 0xDFFF or num > 0x10FFFF:      return '�'
//           if num in _invalid_codepoints:                     return ''
//           return chr(num)
//       else:
//           if s in _html5: return _html5[s]
//           for x in range(len(s)-1, 1, -1):
//               if s[:x] in _html5: return _html5[s[:x]] + s[x:]
//           else:                   return '&' + s
//
// Codepoint-fidelity note: CPython operates on Unicode code points. JS strings are UTF-16, but the
// `_charref` regex matches only ASCII bytes (`#`, hex/decimal digits, and the `[^\t\n\f <&#;]`
// class which can include astral chars). `String.prototype.slice` on the matched group operates on
// UTF-16 code units; because the named-charref group is bounded to {1,32} and matched purely by the
// regex (no manual indexing into astral chars within the algorithm beyond `s[0]`/`s[1]` which are
// always ASCII `#`/`x`/`X`/digits in the numeric branch), the with-/without-semicolon and
// progressively-shorter-prefix logic reproduces CPython exactly for all entity inputs.

