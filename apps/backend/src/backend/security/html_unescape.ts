// CPython `_charref` regex verbatim. Global flag so `String.prototype.replace` walks every match
// (CPython `re.sub` replaces all). The `[^\t\n\f <&#;]` class and the {1,32} bound match CPython
// byte-for-byte (no IGNORECASE; `#[xX]` is the only case-insensitive piece, encoded explicitly).
// Bounded {1,32} quantifier, single alternation level, no nested/ambiguous repetition: linear-time,
// no catastrophic backtracking (detect-unsafe-regex does not fire — no disable directive needed).
const CHARREF = /&(#[0-9]+;?|#[xX][0-9a-fA-F]+;?|[^\t\n\f <&#;]{1,32};?)/g;

// Strip a single trailing ';' if present (CPython `str.rstrip(';')` removes ALL trailing ';', but
// the `_charref` regex captures at most ONE trailing ';' in the numeric branches, so removing one
// is equivalent here; we mirror rstrip semantics anyway for exactness).
function rstripSemicolons(s: string): string {
  let end = s.length;
  while (end > 0 && s.charCodeAt(end - 1) === 0x3b /* ';' */) {
    end -= 1;
  }
  return s.slice(0, end);
}

// CPython `chr(num)`: the Unicode code point as a string. JS `String.fromCodePoint` produces the
// correct UTF-16 encoding (surrogate pair for astral). Surrogates / out-of-range are filtered
// upstream before this is reached, matching CPython's guard ordering.
function chrCodePoint(num: number): string {
  return String.fromCodePoint(num);
}

// Mirror of CPython `_replace_charref` over the captured group-1 string (no leading '&').
function replaceCharref(group1: string): string {
  if (group1.charCodeAt(0) === 0x23 /* '#' */) {
    // numeric charref
    const second = group1.charCodeAt(1);
    let num: number;
    if (second === 0x78 /* 'x' */ || second === 0x58 /* 'X' */) {
      num = Number.parseInt(rstripSemicolons(group1.slice(2)), 16);
    } else {
      num = Number.parseInt(rstripSemicolons(group1.slice(1)), 10);
    }
    const invalid = INVALID_CHARREFS.get(num);
    if (invalid !== undefined) {
      return invalid;
    }
    if ((num >= 0xd800 && num <= 0xdfff) || num > 0x10ffff) {
      return "�";
    }
    if (INVALID_CODEPOINTS.has(num)) {
      return "";
    }
    return chrCodePoint(num);
  }
  // named charref
  const exact = HTML5_ENTITIES.get(group1);
  if (exact !== undefined) {
    return exact;
  }
  // find the longest matching name (as defined by the standard):
  // for x in range(len(s)-1, 1, -1): if s[:x] in html5: return html5[s[:x]] + s[x:]
  for (let x = group1.length - 1; x > 1; x -= 1) {
    const prefix = group1.slice(0, x);
    const matched = HTML5_ENTITIES.get(prefix);
    if (matched !== undefined) {
      return matched + group1.slice(x);
    }
  }
  // else branch: no prefix matched
  return "&" + group1;
}

/**
 * Byte-exact port of CPython `html.unescape`. Converts all named and numeric character references
 * in `s` to the corresponding Unicode characters, using the HTML5 rules for valid AND invalid
 * references. Used by the trust-tier wrapper to canonicalize entity-encoded privileged tags before
 * stripping (so `&lt;diff&gt;` is decoded then stripped alongside the literal form).
 */
export function htmlUnescape(s: string): string {
  if (!s.includes("&")) {
    return s;
  }
  return s.replace(CHARREF, (_full, group1: string) => replaceCharref(group1));
}
